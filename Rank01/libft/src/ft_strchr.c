char	*ft_strchr(const char *s, int c)
{
	int				i;
	unsigned char	d;

	i = 0;
	d = c;
	if (d == 0)
	{
		while (s[i] != 0)
			i++;
		return ((char *)(s + i));
	}
	while (s[i] != 0)
	{
		if (s[i] == d)
			return ((char *)(s + i));
		i++;
	}
	return (0);
}
